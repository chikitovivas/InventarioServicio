    private void ProveedoresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProveedoresActionPerformed
        if(this.Proveedores.getItemCount() != 0){//Entra en la condicion si hay mas de un Item en el ComboBox
            //Buscar el id de Categoria con respecto a la descripcion
            Proveedor pro = new Proveedor(con);

            //Busqueda de DB, para la categoria seleccionada, y llenar los campos
            ResultSet proveedor = pro.getProveedor(Proveedores.getSelectedItem().toString());
            try {
                this.id.setText(proveedor.getString("idProveedor"));
                this.razon_social.setText(proveedor.getString("razon_social"));
                this.rif.setText(proveedor.getString("RIF_Cedula"));
                this.direccion.setText(proveedor.getString("direccion"));
                this.tlf1.setText(proveedor.getString("telefono_1"));
                this.tlf2.setText(proveedor.getString("telefono_2"));
                this.contacto.setText(proveedor.getString("persona_contacto"));
                this.email.setText(proveedor.getString("email"));
            } catch (SQLException ex) {
                Logger.getLogger(AgregarEditar_familia.class.getName()).log(Level.SEVERE, null, ex);
            }

            //Cambios graficos al cambiar una categoria
            this.reanudar();
        }
    }//GEN-LAST:event_ProveedoresActionPerformed

    private void cancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarActionPerformed
        //Reanudo los campos de Categoria a deshabilitados
        this.reanudar();
    }//GEN-LAST:event_cancelarActionPerformed

    private void agregar_proveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregar_proveedorActionPerformed
        //Creamos un nueva Callable (Creamos una variable que guarda funciones)
        Callable<Void> prueba = new Callable<Void>() {
            //Funcion call(), la que se ejecutara desde la variable
            public Void call() {
                reanudar();
                borrarS();
                return null;
            }
        };
        //Abrimos un nuevo Jframe para agregar categoria
        Agregar_proveedor pantalla;
        try {
            pantalla = new Agregar_proveedor(this.Proveedores,this.con,prueba,this.refrescar);
            pantalla.setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(AgregarEditar_proveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_agregar_proveedorActionPerformed

    private void editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarActionPerformed
        //Si en el Combobox Categorias hay items, la funcion editar servira
        //Habilita los campos de descripcion y los botones de guardar y cancelar
        if(this.Proveedores.getSelectedIndex() != -1){
            this.razon_social.enable(true);
            this.rif.enable(true);
            this.direccion.enable(true);
            this.tlf1.enable(true);
            this.tlf2.enable(true);
            this.contacto.enable(true);
            this.email.enable(true);
            this.guardar.setVisible(true);
            this.cancelar.setVisible(true);
            this.editar.setVisible(false);
            this.razon_social.setBackground(Color.white);
            this.rif.setBackground(Color.white);
            this.direccion.setBackground(Color.white);
            this.tlf1.setBackground(Color.white);
            this.tlf2.setBackground(Color.white);
            this.contacto.setBackground(Color.white);
            this.email.setBackground(Color.white);
        }
    }//GEN-LAST:event_editarActionPerformed

    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        //Clase Proveedor
        Proveedor cat = new Proveedor(con);
        //Si se Guarda la categoria Exitosamente
        if(cat.actualizar_proveedor(this.id.getText().toString(),this.razon_social.getText().toString(),this.rif.getText().toString()
            ,this.direccion.getText().toString(),this.tlf1.getText().toString(),this.tlf2.getText().toString()
            ,this.contacto.getText().toString(),this.email.getText().toString())){
            JOptionPane.showMessageDialog(null, "Proveedor actualizado Exitosamente");
            //Remueve todos los Items del list Proveedor
            this.Proveedores.removeAll();
            //Y se agregan las nuevas categorias al comboBox pero actualizado
            try {
                this.refrescar.call();
                //Nueva Clase Categoria
                Proveedor ca = new Proveedor(con);
                //Se obtienen todas las descripciones de las Proveedor
                rs = ca.getAll();
                // Anadimos cada una de las Proveedor al list Proveedor
                while(rs.next()){
                    this.Proveedores.add(rs.getString("razon_social"));
                }

            } catch (SQLException ex) {
                Logger.getLogger(AgregarEditar_proveedor.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(AgregarEditar_proveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
            //Se borran los campos descripcion y id de Proveedor
            this.borrarS();
            //Se borran los campos 
            this.reanudar();
        }else{
            JOptionPane.showMessageDialog(null, "No se actualizo el proveedor. \n Los campos (*) no pueden repetirse","Error al actualizar"
                ,JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_guardarActionPerformed

